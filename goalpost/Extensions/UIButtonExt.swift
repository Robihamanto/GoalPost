import UIKit

extension UIButton {
    
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0, green: 0.8460595012, blue: 1, alpha: 1)
    }
    
    func setDeselectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.4705301523, green: 0.9002516866, blue: 1, alpha: 1)
    }
}
